    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String id =  txtid.getText();
        try {
            pst = con.prepareStatement( "SELECT * FROM info where id = ?" );
            pst.setString(1, id);
            rs = pst.executeQuery();
            if ( rs.next() == true )
            {
                txtname.setText(rs.getString(3));
                txtbatch.setText(rs.getString(4));
                txteml_phn.setText(rs.getString(6));
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Record Not Found");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Stud_Mang_Sys.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

