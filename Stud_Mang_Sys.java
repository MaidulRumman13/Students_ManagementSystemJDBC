    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String id = txtid.getText();
            String name = txtname.getText();
            String batch = txtbatch.getText();
            
            String eml_phn = txteml_phn.getText();
            
            pst = con.prepareStatement("INSERT INTO info(id, name, batch, campus, eml_phn) VALUES(?,?,?,?,?)");
            pst.setString(1, id);
            pst.setString(2, name);
            pst.setString(3, batch);
            pst.setString(4, campus);
            pst.setString(5, eml_phn);
            int k = pst.executeUpdate();
            
            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "Record Addded");
                txtid.setText("");
                txtname.setText("");
                txtbatch.setText("");
                txteml_phn.setText("");
                txtid.requestFocus();
                
                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Record Failed");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Stud_Mang_Sys.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

